<h3>&nbsp;&nbsp;&nbsp;Show Synopsis</h3>

    <p>In the Summer of 1946, the Frake family excitedly anticipates the Iowa State Fair. Melissa and Abel have "blue ribbon" hopes and their children, Margy and Wayne, are attending as young adults, bringing their dreams and cares in tow.  Meeting newspaper reporter Pat and band singer Emily, they realize that life is more than the farm at home and the trip to the fair is more than a simple journey across the state. Entwined in this beautiful love story, <i>State Fair</i> reminds us how our lives can be changed by a rash decision, a chance meeting, or the unexpected. </p>
    <p><i>State Fair</i> features an Academy Award-winning score by Rodgers and Hammerstein, including &#8220;It Might as Well Be Spring&#8221;, &#8220;A Grand Night For Singing&#8221; and &#8220;Our State Fair&#8221;.
