  <h2 id="pageName"><div style="float:right; font-size:small; font-weight: normal"><a href="Calendar.php">return to order a calendar</a>
</div>Calendar Review </h2>
  <div class="feature"><a href="Calendar.php"><img src="../images/2007-Calendar.jpg" alt="... the Sisters and The Cousins and The Aunts ..."" name="GandS_Ladies" width="150" border="0" id="GandS_Ladies" title"... the Sisters and Cousins Aunts ... /></a>
    <div><p style="font-weight: normal;">I have just received my copy of the calendar. As stated above, it is humorous and tasteful, with no exposure of flesh that would deprive any film of a G-rating. I must disagree with one point: I admit to
being a bit of a prurient type, and I am absolutely spellbound. (Ah, to have been a fly on the wall at the photo session...)</p>


<p style="font-weight: normal;">The blurb states that 25 models, a costumer and a photographer participated, all female and all members of the Rose Valley company, with nobody identified by name. The fact that four of the participants are under 40 and five are over 70 is offered. Each of the 12 months of 2007 is decorated with a G&S scene shot in black and white. Each photo features several white roses, with one of the roses tinted pink, somewhere in the foreground. Each is most attractively composed and posed, with some of the shots providing more immediate G&S recognition at sight than others, though each month includes a short synopsis of the month's opera.</p>


<p style="font-weight: normal;">Here are the details:</p>

<p style="font-weight: normal;">January--MIKADO. Three maids with Japanese wigs and open fans. Definitely recognizable.</p>

<p style="font-weight: normal;">February--PIRATES. Ruth in high boots, a Jolly Roger flag and crossed pistols, with one foot on top of a large treasure chest.</p>

<p style="font-weight: normal;">March--GONDOLIERS. Gianetta and Tessa sitting on a love seat playing mandolins, with Casilda standing behind them. A bit of a stretch, but GONDOLIERS offers little in the way of female interaction.</p>

<p style="font-weight: normal;">April--IOLANTHE. A lovely fairy with wings, tiara and star-topped staff in front of a rustic bridge. My favorite.</p>

<p style="font-weight: normal;">May--RUDDIGORE. Rose Maybud with etiquette book and basket, offering an apple to wide-eyed, grinning, stringy-haired Mad Margaret. It's not easy to be Mad Margaret without torn clothing (or any other kind of clothing). Good recognition and great fun.</p>

<p style="font-weight: normal;">June--TRIAL. Angelina with bridal veil, bouquet and full-length see-through gown. Her modern-looking white high-heeled pumps may not be quite right for 1875.</p>

<p style="font-weight: normal;">July--IDA. Five radiant women warriors in horned helmets with battleaxes. Could also be used for a Die
Walkure calendar. Adorable.</p>

<p style="font-weight: normal;">August--YEOMEN. Elsie Maynard with long ribbons and tambourine. They could have used someone (not
necessarily male) in a warder's hat and ruff, or jester's headgear, to pose with her.</p>

<p style="font-weight: normal;">September--PATIENCE. Patience with milk pails confronting Lady Jane playing her cello. Hands down
the best realization of identifiable G&S characters.</p>

<p style="font-weight: normal;">October--SORCERER. Four ladies at a tea table with a flaming cauldron in the center. Could be mistaken for a tame MACBETH with an extra witch.</p>

<p style="font-weight: normal;">November--PINAFORE. Buttercup behind a ship's steering wheel with baskets of provisions (pretty polonies not visible).</p>

<p style="font-weight: normal;">December--PINAFORE. Sixteen ladies, with festive hats and open parasols, toasting with champagne glasses. Looks more like a "traditional" nude charity calendar than a G&S scene, but a fittingly joyous, triumphant finale for the year.</p>

<p style="font-weight: normal;">What more can I say but "Buy this calendar!"? It will make you smile, perhaps even make you laugh, and any other physical reaction is up to you. Best of all, it is faithful in its own special way to the spirit of G&S. A souvenir to treasure.</p>

<p style="font-weight: normal;">Dan Kravetz<br />
Gilbert & Sullivan Society of New York
</p></div></div>
