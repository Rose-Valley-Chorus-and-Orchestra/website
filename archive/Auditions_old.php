    <h4>Show Synopsis</h4>

    <p>Cinderella, forced into a life of household chores by her wickedly cruel stepmother and stepsisters, dreams of a better life. The town crier announces a royal ball at which the Prince of the kingdom intends to find a bride, and Cinderella wishes she could attend. When her stepsisters and stepmother go off to the ball, her Godmother magically grants Cinderella's wish. At the ball, Cinderella and the Prince meet and instantly fall in love, but she abruptly excuses herself at midnight, leaving only a glass slipper on the palace steps. After an extensive search throughout the kingdom, the Prince and Cinderella are magically reunited and blissfully married.</p>
