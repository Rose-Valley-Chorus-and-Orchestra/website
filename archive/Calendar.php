    <p style="font-weight: normal;">This gently wry and artistic calendar is printed on a heavy coated stock in 
two colors using duotone photos with spot color. It is a Centennial Celebration 
fund raiser for Rose Valley Chorus & Orchestra, one of America's oldest 
continuous amateur Gilbert & Sullivan companies, performing since 1907 in the 
Delaware County area of Suburban Philadelphia.</p>

<p style="font-weight: normal;">The calendar is decorated by 12 tastefully nude photographs modeled by the 
women of the organization depicting scenes and  characters from 11 of the 
Gilbert & Sullivan shows - The Mikado, The  Pirates of Penzance, The Gondoliers, 
Iolanthe, Ruddigore, Trial by Jury,  Princess Ida, The Yeomen of the Guard, 
Patience, H.M.S. Pinafore and The  Sorcerer.</p>

<p style="font-weight: normal;">We hope to continue the production of Gilbert & Sullivan and other musical 
theater for at least another century.  All  profits from the sale of these  
calendars directly  support the productions and charitable activities of Rose 
Valley Chorus  & Orchestra, such as our "Music in the Schools" program which helps 
introduce elementary school children to the magic of live theatre.  Rose 
Valley Chorus, Inc. is a 501 (c)(3) non-profit organization as  recognized by the 
IRS. The price includes all shipping charges.</p>

