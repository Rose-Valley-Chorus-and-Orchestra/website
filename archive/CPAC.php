  <h2 id="pageName"><a href="http://www.pacofdelco.org" target="_blank">The PERFORMING ARTS COMPLEX of Delaware County, Inc. </a></h2>
  <div class="story">
    For additional information or to make a contribution to The Preforming Arts Complex of Delaware County, Inc., please visit their Web site at <a href="http://www.pacofdelco.org" target="_blank">www.pacofdelco.org</a>.<br />
    <br />
  </div>
  <div class="feature">
   <h2>Vision Statement</h2>
       We envision a performing arts center, shared by many groups, that will provide a constant location, economies of scale, high production values, and a variety of spaces for community activities of all sorts.
  </div>
  <div class="story">
   <h2>Background</h2>
       Delaware County is rich in nonprofit community theatrical, musical, and dance organizations but many of them lack a permanent home. Affordable suitable performance rental space is hard to find. School auditoriums are usually fully booked with school activities or are not available for non-school groups. The local privately owned facility is too costly for community groups and provides only limited performance space for most groups&#8212;the backstage area, for example, is miniscule.
  </div>
  <div class="story">
   <h2>Organization</h2>
       A board of directors with representatives from some of the performing groups and the community will set policy and overall direction for the center. A small management staff will deal with day-to-day operations such as cleaning, maintenance, and ticket sales.<br />
Individual groups who rent space for performances will be responsible for load-in, load-out, and cleanup; they will store their sets, props, etc., off-site.
  </div>
  <div class="story">
   <h2>Building</h2>
       The central part of the building will be a fully functioning theater with a welcoming lobby and comfortable seating&#8212;in a space adjustable for large and small audiences. The stage will accommodate a symphony orchestra, dance troupe, or a large musical show cast. An orchestra pit will hold about 30 musicians. There will be fly space, useful wings, and dressing and gathering rooms for performers. In addition, there will be a separate small performance space and rehearsal rooms of several sizes to accommodate the needs of different performing groups and for community gatherings. A basic kitchen will accommodate catered functions.
  </div>
  <div class="story">
   <h2>Location</h2>
       The center, located in Delaware County, will be easy to find, close to major highways and, if possible, public  transportation.
  </div>
  <div class="story">
   <h2>Productions</h2>
       The performing groups will offer a varied season of musical shows, drama, concerts, and dance programs. The building will also provide facilities for classes and community or business meetings and functions.
  </div>
  <div class="story">
   <h2>Financing</h2>
       The center will be organized as a nonprofit corporation. It will be self-supporting from rents, fund-raising events, and grants. Initial funding will come from individuals identified by the groups involved, individuals interested in naming  opportunities, and grants.
  </div>
   <div class="story">
   <h2>Summary</h2>
       We envision a building centrally located that supports a variety of excellent productions and community events. The groups performing there will have the advantage of central ticket sales, a permanent venue, and a fully functioning theater&#8212;each group's creativity and time can focus on the artistic aspects of their shows, rather than on finding and equipping a performance site. The general audience will have the opportunity of enjoying a variety of performances in an attractive and comfortable setting.
  </div>
 
