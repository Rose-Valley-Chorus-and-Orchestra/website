  <h2 id="pageName">MUSIC IN SCHOOLS</h2>
  <div class="story">
<p>Are you the kind of person who enjoys entertaining others?   Do you thrill to the sound of laughter and applause from an enthusiastic and appreciative audience?  Perhaps you are an empty nester, a retiree, or just a person looking for something to do in your spare time that is both fun and rewarding.   Sound interesting?  If you love to sing and act and are a seasoned performer who wants to put those talents to good use, we have the perfect opportunity for you.</p>
 
<p>Rose Valley Chorus and Orchestra is looking for volunteers like you to take part in their brand new Music in the Schools program.   This fledgling venture was launched in the Spring of 2006 with a capsulated version of Gilbert and Sullivan's classic operetta, The Pirates of Penzance, a light-hearted and good natured satire of the British aristocracy in Queen Victoria's day. Primarily designed for elementary and middle school aged children, this latest effort represents yet another direction RVCO's Community Outreach activities are taking them in their goal to better serve local patrons in the Delaware and Chester County areas.</p>
 
<p>Do you remember how old you were when you attended your first theatrical production and were bitten by the "show biz" bug?  It might have been a magic show, a dance recital, a concert, the circus, or maybe a school play.  But it was live, and there were real people performing just for you, the audience. It is our intention and fondest hope to introduce today's young boys and girls to this same magical experience.  To 21st century boys and girls, accustomed to electronic games and gadgets, musical theatre can change their lives.  It may very well open their eyes to a different dimension of entertainment and be the spark that ignites a life long love of the performing arts.</p>
 
<p>Modified and adapted to appeal to boys and girls through age 12, The Pirates of Penzance integrates costumes and props with music and song, and incorporates a modest amount of audience participation involving the students. The show has been reduced to a total running time of no more than 35 minutes in order to neatly fit into a standard assembly time slot and is being offered absolutely free of charge.  Based on the overwhelming favorable response from children and teachers alike, it is likely that long-term plans will expand to include other G&S adaptations sometime in the future.</p>
 
<p>Come join in the fun and delight of introducing youngsters to a comic tale of bungled situations, awkward relationships, and confused entanglements as only Gilbert and Sullivan can concoct.  Enjoy watching the expressions on young faces as they react to menacing pirates, clumsy police and fair damsels in distress. If you are interested in learning more about participating in one of our Music in the Schools productions or would like to inquire about us preforming at your school, please call us at 610-565-5010, <a href="mailto:musicinschools@rvco.org">e-mail</a>, or write RVCO, PO Box 414, Media, Pa. 19063</p>
 
 
<p>For those of you unfamiliar with our organization, a brief history follows: Founded in 1907, Rose Valley Chorus and Orchestra was originally known as the Folk of Rose Valley.  It consisted of a small band of residents locally known for performing a Gilbert and Sullivan light opera once a year with piano accompaniment.  Over the past 100 years, this small community theatre group has grown considerably in body and repertoire.  Today we present two professionally staged productions; one traditional G&S operetta and one modern Broadway show.  In addition, we also feature a Road Show troupe of selected soloists who perform individually and as a group.   For further information on any of our activities or to order tickets for our current show, please contact us at the above telephone number or address.</p>

