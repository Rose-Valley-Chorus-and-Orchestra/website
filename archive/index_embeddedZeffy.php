                            <p>When Frederic was a youngster, his nursemaid, Ruth, was told to apprentice him to a pilot.  But, being hard of hearing, Ruth mistook the word and apprenticed him to a band of pirates.  To atone for her error, Ruth remained with Frederic as a maid-of-all-work.  Frederic loathed the trade to which he had been bound, but served dutifully.  As the curtain rises, we find Frederic celebrating his 21st birthday, which ends his indenture.  He is preparing to leave the band and devote himself to the extermination of piracy.</p>
                              <p>Ruth begs him to take her with him as his wife.  Having seen no other women, Frederic cannot compare her to others and consents to her wishes.  Just then, a group of young ladies, all wards of Major-General Stanley, appear on the scene.  Amazed by their beauty, Frederic renounces Ruth.  When the ladies refuse Frederic's invitation of love, Mabel steps forward.  The rest of the pirates appear, seize the ladies, and threaten immediate marriage.  The Major-General arrives and, having heard of the famous Pirates of Penzance, plays on their sympathy by claiming to be an orphan.  The pirates, being orphans themselves, never attack other orphans and let the Major-General go.</p>
                              <p>This lie troubles the Major-General's conscience and at the beginning of the second act he broods in his Gothic ruin.  He is comforted by his wards and Frederic's plan to lead a band of police against the pirates.</p>
                              <p>However, the rejected Ruth finds a loophole in Frederic's contract.  She and the Pirate King tell him that since he was born on February 29, he has not strictly reached his 21st birthday.  Frederic reluctantly, but dutifully, returns to the pirate band and tells them of the Major-General's lie.  The pirates seize the Major-General and a battle ensues between the pirates and the police who have come to the rescue.  The pirates initially overcome the police, but then yield in Queen Victoria's name.  Ruth reveals that the pirates are all noblemen who have gone wrong-they are pardoned and permitted to marry the Major-General's wards.</p>
